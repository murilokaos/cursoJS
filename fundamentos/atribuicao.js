const a = 7 // atribuição do valor 7 na variável
let b = 3

b += a // atribuição aditiva b = b + a = 10
console.log(b)

b -= 4 // atribuição subtrativa -> b = b - 4 = 6
console.log(b)

b *= 2 // atribuição multiplicativa => b = b * 2 = 12
console.log(b)

b /= 2 // atribuição divisiva => b = b / 2 = 6
console.log(b)

b %= 2 // atribuição modular => b = resto de b % 2 (par = 0, impar = 1)
console.log(b)